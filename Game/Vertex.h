
struct Vertex
{
	struct
	{
		float x;
		float y;
	} pos;
	struct
	{
		unsigned char r;
		unsigned char g;
		unsigned char b;
		unsigned char a;
	} color;
};

struct Vertex3D
{
	struct
	{
		float x;
		float y;
		float z;
	} pos;
	struct
	{
		unsigned char r;
		unsigned char g;
		unsigned char b;
		unsigned char a;
	} color;
};